function applyTranslations(lang) {
    const t = translations[lang];
    if (!t) return;

    // Nav
    document.querySelectorAll('.nav-links li a').forEach((el, index) => {
        if (t.nav[index]) el.textContent = t.nav[index];
    });

    // Hero
    document.querySelector('.hero-content .tagline').textContent = t.hero.tagline;
    document.querySelector('.hero-content h1').innerHTML = t.hero.title;
    document.querySelector('.hero-content .description').textContent = t.hero.desc;
    document.querySelector('.cta-group .btn.primary').textContent = t.hero.btn1;
    document.querySelector('.cta-group .btn.secondary').textContent = t.hero.btn2;

    // Divisions
    document.querySelector('#divisiones .section-title').textContent = t.divisions.title;
    document.querySelectorAll('.card').forEach((card, i) => {
        if (t.divisions.cards[i]) {
            card.querySelector('.card-front h3').textContent = t.divisions.cards[i].title;
            card.querySelector('.card-back h3').textContent = t.divisions.cards[i].title;
            card.querySelector('.flip-hint').textContent = t.divisions.hints;
            card.querySelector('.card-back p').textContent = t.divisions.cards[i].desc;
        }
    });

    // Philosophy
    const philItems = document.querySelectorAll('.philosophy-item');
    if (philItems.length >= 2) {
        philItems[0].querySelector('h2').textContent = t.philosophy.mTitle;
        philItems[0].querySelector('p').textContent = t.philosophy.mDesc;
        philItems[1].querySelector('h2').textContent = t.philosophy.vTitle;
        philItems[1].querySelector('p').textContent = t.philosophy.vDesc;
    }

    // Governance
    const govItems = document.querySelectorAll('.governance-item');
    if (govItems.length >= 2) {
        govItems[0].querySelector('h2').textContent = t.governance.govTitle;
        const govP1s = govItems[0].querySelectorAll('p');
        if (govP1s[0]) govP1s[0].textContent = t.governance.govP1;
        if (govP1s[1]) govP1s[1].textContent = t.governance.govP2;

        govItems[1].querySelector('h2').textContent = t.governance.respTitle;
        const govP2s = govItems[1].querySelectorAll('p');
        if (govP2s[0]) govP2s[0].textContent = t.governance.respP1;
        if (govP2s[1]) govP2s[1].textContent = t.governance.respP2;
    }

    // Assets
    document.querySelector('.corporate-assets h2').textContent = t.assets.title;
    document.querySelector('.corporate-assets .content-left p').textContent = t.assets.leftP;
    document.querySelector('.corporate-assets .content-right p').textContent = t.assets.rightP;

    // About
    document.querySelector('.about-header .tagline').textContent = t.about.tagline;
    
    // For typing effect, we update data-text or just text content if already typed
    const titleEl = document.querySelector('.about-header h2');
    const descEl = document.querySelector('.about-content p');
    if (titleEl) {
        titleEl.setAttribute('data-text', t.about.typingTitle);
        titleEl.innerHTML = t.about.typingTitle; // Instant update
    }
    if (descEl) {
        descEl.setAttribute('data-text', t.about.typingText);
        descEl.innerHTML = t.about.typingText; // Instant update
    }

    document.querySelectorAll('.stat-item .stat-label').forEach((el, index) => {
        if (t.about.stats[index]) el.textContent = t.about.stats[index];
    });

    // Pillars
    document.querySelectorAll('.pillar-item').forEach((el, index) => {
        if (t.pillars[index]) {
            el.querySelector('h3').textContent = t.pillars[index].title;
            el.querySelector('p').textContent = t.pillars[index].desc;
        }
    });

    // Contact
    document.querySelector('.footer-info h2').textContent = t.contact.title;
    document.querySelector('.footer-info p').textContent = t.contact.desc;
    
    const contactForm = document.getElementById('contact-form');
    if (contactForm) {
        contactForm.querySelector('input[name="nombre"]').placeholder = t.contact.phName;
        contactForm.querySelector('input[name="email"]').placeholder = t.contact.phEmail;
        contactForm.querySelector('textarea[name="mensaje"]').placeholder = t.contact.phProj;
        contactForm.querySelector('button[type="submit"]').textContent = t.contact.btnSubmit;
    }

    // Footer
    document.querySelector('.footer-brand p').textContent = t.footer.brandText;
    const footerHeaders = document.querySelectorAll('.footer-links h3');
    if (footerHeaders.length >= 2) {
        footerHeaders[0].textContent = t.footer.divTitle;
        footerHeaders[1].textContent = t.footer.corpTitle;
    }
    document.querySelectorAll('.footer-links:nth-of-type(3) ul li a').forEach((el, index) => {
        if (t.footer.corpLinks[index]) el.textContent = t.footer.corpLinks[index];
    });
    
    // Divisions footer links
    document.querySelectorAll('.footer-links:nth-of-type(2) ul li a').forEach((el, index) => {
        if (t.divisions.cards[index]) el.textContent = t.divisions.cards[index].title;
    });

    document.querySelector('.footer-bottom .copyright').textContent = t.footer.copyright;

    // Update Lang button Text
    const langBtn = document.querySelector('.btn-lang');
    if (langBtn) {
        langBtn.textContent = lang === 'es' ? 'EN' : 'ES'; // Si está en EN, muestra botón para ES
    }
}

document.addEventListener('DOMContentLoaded', () => {
    // Initialize Language from localStorage or default to 'es'
    let currentLang = localStorage.getItem('pg_lang') || 'es';
    
    // Apply immediately if not default
    if (currentLang !== 'es') {
        applyTranslations(currentLang);
    }

    // Toggle button event listener
    const langBtn = document.querySelector('.btn-lang');
    if (langBtn) {
        // Set initial text on load correctly
        langBtn.textContent = currentLang === 'es' ? 'EN' : 'ES';
        
        langBtn.addEventListener('click', () => {
            currentLang = currentLang === 'es' ? 'en' : 'es';
            localStorage.setItem('pg_lang', currentLang);
            applyTranslations(currentLang);
        });
    }
});